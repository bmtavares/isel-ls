private val logger = LoggerFactory.getLogger("pt.isel.ls.http.HTTPServer")


fun logRequest(request: Request) {
    logger.info(
        "incoming request: method={}, uri={}, content-type={} accept={}",
        request.method,
        request.uri,
        request.header("content-type"),
        request.header("accept")
    )
}

